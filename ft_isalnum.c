#include "libft.h"

int		ft_isalnum(int c)
{
	int	res;

	res = 0;
	if (c >= 48 && c <= 57)
	{
		res = 8;
	}
	else if (c >= 65 && c <= 90)
	{
		res = 8;
	}
	else if (c >= 97 && c <= 122)
	{
		res = 8;
	}
	return (res);
}
