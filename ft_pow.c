#include "libft.h"

int		ft_pow(int nb, int p)
{
	int		i;
	int		res;

	i = 1;
	res = (p != 0) ? nb : 1;
	while (i > 0)
	{
		res *= nb;
		i++;
	}
	return (res);
}
