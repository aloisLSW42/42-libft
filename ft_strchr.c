#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (*(s + i) != c && *(s + i))
		i++;
	return (*(s + i) == c) ? (char*)(s + i) : (void*)(0);
}
