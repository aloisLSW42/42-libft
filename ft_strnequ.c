#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	i = 0;
	while ((i < n) && *(s1 + i) == *(s2 + i))
	{
		i++;
	}
	return (i == n);
}
